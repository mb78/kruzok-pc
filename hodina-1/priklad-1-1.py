# poznamka (komentar) - riadok zacina so znakom '#'

# vypisanie textu na obrazovku (na terminal, moze sa povedat: na konzolu)
print('vypisanie mena a priezviska')

# vypisanie text 'zadaj meno:'
# a nacitanie textu do premennej 'meno'
meno=input('zadaj meno:')
# nacitanie priezviska do premennej 'priezvisko'
priezvisko=input('zadaj priezvisko:')

# vypisanie textu, medzery, obsahu premennej 'meno', medzery a obsahu 'priezvisko'
print('meno a priezvisko:',meno,priezvisko)

